public class bucles {
    public static void main (String[] args){

        //for 

        for(int i = 1; i <= 5; i++){
            System.out.println(i + ": programacion orientada a objetos");
        }

        System.out.println("--------------------------------");

        //while
        int j = 1;
        while(j <= 5){
            System.out.println(j + ": POO");
            j++;
        }
    }
}